function generateHexCode(){
    const hexDigits = "0123456789ABCDGF";
    const randomNumber = Math.floor(Math.random() * 16);
    const hexDigit = hexDigits[randomNumber];    
    return hexDigit;
}

export function generateToken(){ 
    let hexCode = "";
    for (let i = 0; i < 10; i++) {
        hexCode += generateHexCode();
    }

    return hexCode;
}